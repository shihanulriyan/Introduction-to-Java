public class IncrementDecrement{
    public static void main(String[] args){

        int a = 25;

        // Increment
        System.out.println(++a);  // 12 (aghe bare, pore print)
        System.out.println(a++);  // 10 (aghe print, pore bare)
        

        // Decrement
        System.out.println(--a);  // 10 (aghe kome, pore print)
        System.out.println(a--);  // 12 (aghe print, pore kome)
    }
}